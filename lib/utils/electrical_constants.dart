// ── Funciones utilitarias ────────────────────────────────────────────────────

double getLoadCurrent(
    {required String loadType,
    required double load,
    required double fp,
    required String type,
    required double voltage}) {
  double v = type == 'trifasico' ? sqrt(3) * voltage : voltage;
  if (loadType == 'kVA')
    return double.parse(((load * 1000) / v).toStringAsFixed(2));
  final loadKVA = load / fp;
  return double.parse(((loadKVA * 1000) / v).toStringAsFixed(2));
}

double getLoadPower(
    {required double current, required String type, required double voltage}) {
  final v = type == 'trifasico' ? sqrt(3) * voltage : voltage;
  return double.parse(((current * v) / 1000).toStringAsFixed(2));
}
